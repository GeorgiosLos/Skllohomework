# main_script.py

import geometry

# Calculate areas using the geometry module
square_area_result = geometry.square_area(5)
rectangle_area_result = geometry.rectangle_area(4, 6)
triangle_area_result = geometry.triangle_area(3, 8)
circle_area_result = geometry.circle_area(2)

# Display results
print("Square Area:", square_area_result)
print("Rectangle Area:", rectangle_area_result)
print("Triangle Area:", triangle_area_result)
print("Circle Area:", circle_area_result)
